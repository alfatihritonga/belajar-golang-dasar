package main

import "fmt"

func main() {
	fmt.Println("Muhammad A-fatih Ritonga")
	fmt.Println(len("Muhammad A-fatih Ritonga"))
	fmt.Println("Muhammad Al-fatih Ritonga"[0])
	fmt.Println("Muhammad Al-fatih Ritonga"[10])
}

// Note

// Fucntion untuk String
// - len("string") => menghitung panjang karakter *karakter "whitespace juga dihitung"
// - "string"[index] => mendapat karakter berdasarkan posisi indexnya
